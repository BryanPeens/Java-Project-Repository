    private void tblOrdersMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblOrdersMouseClicked
         DataHandler dh = new DataHandler();
        

        try {
            dh.connect();
            dh.st = null;
            dh.rs =  null;
            int row = tblOrders.getSelectedRow();
            String tableClick = (tblOrders.getModel().getValueAt(row, 0).toString());
            String query = "SELECT * FROM v_ordersd WHERE orderid = '" + tableClick + "' ";
            dh.st = dh.conn.prepareStatement(query);
            dh.rs = dh.st.executeQuery(query);

            if (dh.rs.next())
            {
                String add1 = dh.rs.getString("orderid");
                txtidReq.setText(add1);
                String add2 = dh.rs.getString("product");
                txtnameReq.setText(add2);
                String add6 = dh.rs.getString("Quantity");
                txtquantReq.setText(add6);
//lblTxtMaxValue.setText(add6);
                String add7 = dh.rs.getString("requestedby");
                txtuserRq.setText(add7);
            }
        }
        catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_tblOrdersMouseClicked

