    public void populateTable() 
    {
        DataHandler dh = new DataHandler();
        List<orderRequest> product = null;
        try {
            product = b.GetRequests();
        } catch (Exception ex) {
            Logger.getLogger(ManageStockPage.class.getName()).log(Level.SEVERE, null, ex);
        }
        DefaultTableModel tm = (DefaultTableModel) tblOrders.getModel();
        
        int rowCount = tm.getRowCount();
        for (int i = rowCount - 1; i >= 0; i--) 
        {
              tm.removeRow(i);
        }
        
        for (orderRequest product1 : product)
        {
            Object[] obj = new Object[8];
            obj[0] = product1.getOrderId();
            obj[1] = product1.getProductName();
            obj[2] = product1.getQuantity();
            obj[3] = product1.getUsername();
            

           tm.addRow(obj);
        }
        tblOrders.setModel(tm);
    }
